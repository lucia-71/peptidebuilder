def calc_distance(ref, atom):
    """
        Calculates the distance between two points

        Args:
            ref: a reference point
            atom: the new atom coordinates
        Returns:
            distance: the distance
    """
    distance = 0.0
    for i in range(3):
        distance += (ref[i] - atom[i]) ** 2
    distance = np.sqrt(distance)
    return distance

